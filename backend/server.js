// GET PENDING SUBMISSIONS FOR A PROFESSOR'S CLASSROOMS
app.get('/api/submissions/pending', auth, async (req, res) => {
    try {
        if (req.user.role !== 'professor') {
            return res.status(403).json({ error: 'Access denied. Professors only.' });
        }

        // 1. Find all classrooms owned by this professor
        const classrooms = await Classroom.find({ professor: req.user.id });
        const classroomIds = classrooms.map(c => c._id);

        // 2. Find challenges linked to those classrooms
        const challenges = await Challenge.find({ classroom: { $in: classroomIds } });
        const challengeIds = challenges.map(ch => ch._id);

        // 3. Get pending submissions for those challenges, populated with Student & Challenge data
        const pendingSubmissions = await Submission.find({
            challenge: { $in: challengeIds },
            status: 'pending'
        })
        .populate('student', 'name email')
        .populate('challenge', 'title points');

        res.status(200).json(pendingSubmissions);
    } catch (error) {
        console.error("Fetch pending error:", error);
        res.status(500).json({ error: 'Failed to fetch grading queue.' });
    }
});

// APPROVE A SUBMISSION & AWARD XP
app.post('/api/submissions/:submissionId/approve', auth, async (req, res) => {
    try {
        if (req.user.role !== 'professor') {
            return res.status(403).json({ error: 'Access denied. Professors only.' });
        }

        // 1. Find the submission and explicitly pull in the challenge data
        const submission = await Submission.findById(req.params.submissionId).populate('challenge');
        
        if (!submission) {
            return res.status(404).json({ error: 'Submission not found.' });
        }
        if (submission.status !== 'pending') {
            return res.status(400).json({ error: 'This submission has already been graded.' });
        }

        // 2. Award the points and change the status
        submission.status = 'approved';
        submission.pointsAwarded = submission.challenge.points; 
        
        await submission.save();

        res.status(200).json({ 
            message: `Successfully awarded ${submission.pointsAwarded} XP!`,
            submission 
        });
    } catch (error) {
        console.error("Grading error:", error);
        res.status(500).json({ error: 'Failed to approve submission.' });
    }
});

app.post('/api/auth/register',async (req,res)=>{
    try{
        const{name,email,password,role}=req.body;
        const existingUser=await User.findOne({email});
        if(existingUser){
            return res.status(400).json({error:'Email is already registered!'})
        }
        //encrypting the password!!
        const salt=await bcrypt.genSalt(10);
        const hashedPassword=await bcrypt.hash(password,salt);

        //create user with new hashed password!
        const newUser=new User({
            name,
            email,
            password:hashedPassword,
            role:role || 'student'
        })
        await newUser.save();
        res.status(201).json({message:'user registered successfully!'})
    }catch(error){
        console.error('registration error',error);
        res.status(500).json({error:'Server error during registration!'})
    }
})
//login the user
app.post('/api/auth/login',async(req,res)=>{
    try{
        const{email,password}=req.body;

        const user=await User.findOne({email});
        if(!user){
            return res.status(400).json({error:'Invalid email or password!'})
        }
        const isMatch=await bcrypt.compare(password,user.password)
        if(!isMatch){
            return res.status(400).json({error:'Invalid email or password!'})
        }
        //generating the JWT
        const token=jwt.sign({id:user._id,role:user.role},process.env.JWT_SECRET,{expiresIn:'1d'})
        res.status(200).json({
            token,
            user:{
                id:user._id,
                name:user.name,
                role:user.role,
                xp:user.xp
            }
        })    
    }catch(error){
        console.error('login error',error);
        res.status(500).json({error:'Server error during login'})
    }
})
