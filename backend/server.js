app.get('/api/classrooms/:classroomId/leaderboard',auth,async(req,res)=>{
    try{
        const{classroomId}=req.params;
        //getting all challenges for a particular classroom
        const challenges=await Challenge.find({classroom:classroomId})
        const challengeIds=challenges.map(ch=>ch._id);
        if(challengeIds.length===0){
            return res.status(200).json([]) //no challenges or empty leaderboard
        }

        const leaderboard=await Submission.aggregate([
          //stage 1: look for approved submissions only
          {$match:{challenge:{$in:challengeIds},status:'approved'}},
          //stage 2:group by studentId and sum up their points
          {$group:{_id:'$student',totalXP:{$sum:'$pointsAwarded'}}},
          //stage3: sort by totalXP in descending order
          {$sort:{totalXP:-1}},
          //stage4: fetch student details from Users collection
          {$lookup:{//lookup returns an array hence we need to use unwind to extract the object inside
            from:'users',
            localField:'_id',
            foreignField:'_id',
            as:'studentInfo'
          }},
          //stage5: unpack the array and format the final output
          { $unwind:'$studentInfo'},
          {$project:{
            _id:1,//1 means to keep it 0 means to not keep it
            totalXP:1,
            name:'$studentInfo.name',
            email:'$studentInfo.email'
          }}  
        ])
        res.status(200).json(leaderboard);
    }
    catch (error) {
        console.error("Leaderboard error:", error);
        res.status(500).json({ error: 'Failed to generate leaderboard.' });
    }
})
// GET GLOBAL LEADERBOARD (The Hall of Fame)
app.get('/api/leaderboard/global', auth, async (req, res) => {
    try {
        const globalLeaderboard = await Submission.aggregate([
            // Stage 1: The Bouncer - Only let APPROVED submissions through (from ANY class)
            { $match: { status: 'approved' } },
            
            // Stage 2: The Accountant - Group by student, sum ALL points they've ever earned
            { $group: { _id: '$student', totalXP: { $sum: '$pointsAwarded' } } },
            
            // Stage 3: The Ranker - Sort highest XP to the top
            { $sort: { totalXP: -1 } },
            
            // Stage 4 & 5: The Matchmaker & Unpacker - Attach the student's profile info
            { $lookup: { 
                from: 'users', 
                localField: '_id', 
                foreignField: '_id', 
                as: 'studentInfo' 
            }},
            { $unwind: '$studentInfo' },
            
            // Stage 6: The Cleaner - Format for the frontend
            { $project: { 
                _id: 1, 
                totalXP: 1, 
                name: '$studentInfo.name', 
                email: '$studentInfo.email' 
            }}
        ]);

        res.status(200).json(globalLeaderboard);
    } catch (error) {
        console.error("Global Leaderboard error:", error);
        res.status(500).json({ error: 'Failed to generate global leaderboard.' });
    }
});
